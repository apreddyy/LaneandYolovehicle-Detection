#include "laneyolo.hpp"

void laneprocessing::resize_frame(const cv::cuda::GpuMat& src,
                                  cv::cuda::GpuMat& dst, int resize_height,
                                  int resize_width) {
  cv::cuda::resize(src, dst, cv::Size(resize_width, resize_height),
                   cv::INTER_LINEAR);
  return;
}
